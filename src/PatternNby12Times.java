import java.util.*;
public class PatternNby12Times {
	public static void main(String[] args){
	    Scanner sc = new Scanner(System.in);
	    
	    int n =sc.nextInt();
	    
	    for(int j = 0 ; j<3; j ++){
	    for( int i = 1; i <= n ; i++)
	           {
	        System.out.print("*");
	    }
	      System.out.println();
	    }
	   
	}    
	   

}
